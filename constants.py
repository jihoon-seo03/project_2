def c4(n):
    """표본표준편차 → 모표준편차 보정 불편화 상수."""
    n = int(round(n))
    if n < 2:
        return np.nan
    if n in _C4:
        return _C4[n]
    return np.sqrt(2.0 / (n - 1)) * np.exp(gammaln(n / 2.0) - gammaln((n - 1) / 2.0))


def d2(n):
    """범위 → 표준편차 보정 상수 E[R]/σ."""
    n = int(round(n))
    if n in _D2:
        return _D2[n]
    if n > 25:
        return 3.4873 + 0.0250141 * n - 0.00009823 * n ** 2
    return np.nan


def d3(n):
    """범위 W=R/σ 의 표준편차 상수 (소문자 d3)."""
    n = int(round(n))
    if n in _d3:
        return _d3[n]
    if n > 25:
        return _d3[25]
    return np.nan


def A2(n):
    n = int(round(n))
    return _A2[n] if n in _A2 else 3.0 / (d2(n) * np.sqrt(n))


def A3(n):
    n = int(round(n))
    return _A3[n] if n in _A3 else 3.0 / (c4(n) * np.sqrt(n))


def D3(n):
    n = int(round(n))
    if n in _D3:
        return _D3[n]
    return max(0.0, 1.0 - 3.0 * d3(n) / d2(n))


def D4(n):
    n = int(round(n))
    if n in _D4:
        return _D4[n]
    return 1.0 + 3.0 * d3(n) / d2(n)


def B3(n):
    n = int(round(n))
    if n in _B3:
        return _B3[n]
    cc = c4(n)
    return max(0.0, 1.0 - 3.0 * np.sqrt(1 - cc ** 2) / cc)


def B4(n):
    n = int(round(n))
    if n in _B4:
        return _B4[n]
    cc = c4(n)
    return 1.0 + 3.0 * np.sqrt(1 - cc ** 2) / cc
